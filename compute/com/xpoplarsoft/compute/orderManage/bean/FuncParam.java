package com.xpoplarsoft.compute.orderManage.bean;

public class FuncParam {

	private String satMid;
	private String paramName;
	private String paramType;
	private String paramValue;
	private String paramHideValue;
	public String getSatMid() {
		return satMid;
	}
	public void setSatMid(String satMid) {
		this.satMid = satMid;
	}
	public String getParamName() {
		return paramName;
	}
	public void setParamName(String paramName) {
		this.paramName = paramName;
	}
	public String getParamValue() {
		return paramValue;
	}
	public void setParamValue(String paramValue) {
		this.paramValue = paramValue;
	}
	public String getParamHideValue() {
		return paramHideValue;
	}
	public void setParamHideValue(String paramHideValue) {
		this.paramHideValue = paramHideValue;
	}
	public String getParamType() {
		return paramType;
	}
	public void setParamType(String paramType) {
		this.paramType = paramType;
	}
	
}
